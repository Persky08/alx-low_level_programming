#include <stdio.h>

/**
 * add - a function that adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a+b
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * mul - a function that multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a-b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - a function finds the mod two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a-b
 */
int mod(int a, int b)
{
	return (a % b);
}


/**
 * sub - a function that subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a-b
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * div - a function that divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a-b
 */
int div(int a, int b)
{
	return (a / b);
}
