#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: first number
 * @b:second number
 *
 * Return: a+b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a-b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function to multiply to numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: a*b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function to divide two numbers
 * @a: the numerator
 * @b: the denominator
 *
 * Return: a/b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function to find the mod of a number
 * @a: the number in question
 * @b: the modulo we in are working in
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
